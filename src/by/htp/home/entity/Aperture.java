package by.htp.home.entity;

public abstract class Aperture {

	private int width;
	private int height;
	
	public Aperture() {
		
	}
	
	public int getWidth() {
		return width;
	}
	
	public void setWidth(int width) {
		this.width = width;
	}
	
	public int getHeight() {
		return height;
	}
	
	public void setHeight(int height) {
		this.height = height;
	}
}
